// MARK: revertByIdsBaiduApp
/** 根据 ids 还原 百度应用 */
export async function revertByIdsBaiduApp(
  ids: BaiduAppId[],
  options?: {
    is_debug?: boolean;
  },
): Promise<number> {
  
  const table = "baidu_baidu_app";
  const method = "revertByIdsBaiduApp";
  
  const is_debug = get_is_debug(options?.is_debug);
  
  if (is_debug !== false) {
    let msg = `${ table }.${ method }:`;
    if (ids) {
      msg += ` ids:${ JSON.stringify(ids) }`;
    }
    if (options && Object.keys(options).length > 0) {
      msg += ` options:${ JSON.stringify(options) }`;
    }
    log(msg);
    options = options ?? { };
    options.is_debug = false;
  }
  
  if (!ids || !ids.length) {
    return 0;
  }
  
  await delCacheBaiduApp();
  
  let num = 0;
  for (let i = 0; i < ids.length; i++) {
    const id = ids[i];
    let old_model = await findOneBaiduApp(
      {
        id,
        is_deleted: 1,
      },
      undefined,
      options,
    );
    if (!old_model) {
      old_model = await findByIdBaiduApp(
        id,
        options,
      );
    }
    if (!old_model) {
      continue;
    }
    {
      const input = {
        ...old_model,
        id: undefined,
      } as BaiduAppInput;
      const models = await findByUniqueBaiduApp(input, options);
      for (const model of models) {
        if (model.id === id) {
          continue;
        }
        throw "此 百度应用 已经存在";
      }
    }
    const args = new QueryArgs();
    const sql = `update baidu_baidu_app set is_deleted=0 where id=${ args.push(id) } limit 1`;
    const result = await execute(sql, args);
    num += result.affectedRows;
  }
  
  await delCacheBaiduApp();
  
  return num;
}

