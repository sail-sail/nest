<script lang="ts" setup>
type OptionType = {
  label: string;
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  value: any;
};

// eslint-disable-next-line @typescript-eslint/no-explicit-any
type OptionsMap = (item: any) => OptionType;

const emit = defineEmits<{
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  (e: "update:modelValue", value?: any): void,
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  (e: "data", data: any[]): void,
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  (e: "confirm", value?: any): void,
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  (e: "change", value?: any): void,
  (e: "clear"): void,
}>();

const props = withDefaults(
  defineProps<{
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    method: () => Promise<any[]>; // 用于获取数据的方法
    optionsMap?: OptionsMap;
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    modelValue?: any;
    options4SelectV2?: OptionType[];
    placeholder?: string;
    height?: string;
    initData?: boolean;
    pageInited?: boolean;
    clearable?: boolean;
    multiple?: boolean;
    readonly?: boolean;
  }>(),
  {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    optionsMap: function(item: any) {
      const item2 = item as { lbl: string; id: string; };
      return {
        label: item2.lbl,
        value: item2.id,
      };
    },
    modelValue: undefined,
    options4SelectV2: undefined,
    placeholder: "",
    height: "auto",
    initData: true,
    pageInited: true,
    clearable: true,
    multiple: false,
    readonly: false,
  },
);

const inited = ref(false);
// eslint-disable-next-line @typescript-eslint/no-explicit-any
const data = ref<any[]>([ ]);
const options4SelectV2 = ref<OptionType[]>(props.options4SelectV2 || [ ]);

const searchStr = ref("");

const options4SelectV2Computed = computed(() => {
  if (searchStr.value) {
    return options4SelectV2.value.filter((item) => item.label.includes(searchStr.value));
  }
  return options4SelectV2.value;
});

const isTagExpanded = ref(false);

function onExpandTag(event: Event) {
  event?.stopPropagation?.();
  isTagExpanded.value = !isTagExpanded.value;
}

const selectedValue = ref(props.modelValue);
  
watch(
  () => props.modelValue,
  (val) => {
    selectedValue.value = val;
  },
);

watch(
  () => props.readonly,
  () => {
    if (props.readonly) {
      showPicker.value = false;
    }
  },
);

const selectedValueArr = computed(() => {
  if (selectedValue.value == null) {
    return [ ];
  }
  if (props.multiple) {
    if (Array.isArray(selectedValue.value)) {
      return selectedValue.value as string[];
    } else {
      return [ selectedValue.value as string ];
    }
  }
  return [ selectedValue.value as string ];
});

function onSelect(value: string) {
  if (props.multiple) {
    if (selectedValueArr.value.includes(value)) {
      selectedValue.value = selectedValueArr.value.filter((item) => item !== value);
    } else {
      selectedValue.value = [ ...selectedValueArr.value, value ];
    }
  } else {
    selectedValue.value = value;
  }
}

