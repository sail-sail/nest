    un-box-border
    un-gap="2"
    @click="onClick"
  >
    
    <template
      v-if="(props.multiple || modelLabels[0]) && (!props.multiple || modelLabels.length > 0)"
    >
      
      <text
        v-if="!props.multiple || modelLabels.length === 1"
        un-cursor="pointer"
      >
        {{ modelLabels[0] || '' }}
      </text>
      
      <template
        v-else
      >
        
        <template
