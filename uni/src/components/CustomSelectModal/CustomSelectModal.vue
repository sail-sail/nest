        </template>
        
        <template v-else>
          
          <tm-tag
            skin="outlined"
            color="info"
          >
            {{ modelLabels[0] }}
          </tm-tag>
          
          <tm-tag
            v-if="modelLabels.length > 1"
            skin="thin"
            color="info"
            un-cursor="pointer"
            @tap.stop=""
            @click.stop="onExpandTag"
          >
            <view
              v-if="modelLabels.length > 1"
              un-text="gray-400"
            >
              +{{ modelLabels.length - 1 }}
            </view>
          </tm-tag>
          
        </template>
        
      </template>
      
    </template>
    
    <text
      v-else
      un-text="gray"
      un-cursor="pointer"
    >
      {{ props.pageInited ? (props.placeholder || '') : '' }}
    </text>
    
    <view
      un-flex="[1_0_0]"
      un-overflow-hidden
    ></view>
  
    <view
      v-if="props.clearable && !props.readonly && !modelValueIsEmpty"
      @tap.stop=""
      @click="onClear"
    >
      <tm-icon
        _style="transition:color 0.24s"
        :size="30"
        color="#b1b1b1"
        name="close-circle-fill"
        @tap.stop=""
        @click="onClear"
      >
      </tm-icon>
    </view>
    
    <slot name="right"></slot>
    
    <tm-icon
      v-if="!props.readonly"
      :size="42"
      color="#b1b1b1"
      name="arrow-right-s-line"
    ></tm-icon>
    
  </view>
  
  <tm-modal
    v-model:show="showPicker"
    :closeable="true"
    :height="props.height"
    :title="props.placeholder || '请选择'"
    disabled-scroll
    show-close
    :show-footer="false"
    :content-padding="0"
    max-height="90%"
    :overlay-click="false"
    v-bind="$attrs"
  >
    
    <view
      un-h="full"
      un-flex="~ auto col"
    >
      
      <view
        v-if="options4SelectV2.length > 5"
        un-p="t-2"
        un-box-border
        un-m="x-4"
      >
        <tm-input
          v-model="searchStr"
          un-w="full"
          placeholder="请输入搜索内容"
          clearable
        ></tm-input>
      </view>
      
      <scroll-view
        un-flex="~ auto col"
        un-overflow-hidden
        scroll-y
        :rebound="false"
        :scroll-into-view="scrollIntoViewId"
        :scroll-with-animation="true"
      >
          
        <view
          v-for="item of options4SelectV2Computed"
          :id="'a' + item.value"
          :key="item.value"
          :title="item.label"
          un-m="x-4"
          un-p="y-4"
          un-box-border
          un-flex="~"
          un-items="center"
          un-gap="2"
          un-b="0 b-1 solid #e6e6e6"
          :style="{
            'color': selectedValueArr.includes(item.value) ? '#0579ff' : undefined,
          }"
          @click="onSelect(item.value)"
        >
                
          <view
            un-flex="~ [1_0_0]"
            un-overflow-hidden
            un-items="center"
            un-m="l-4"
          >
            {{ item.label }}
          </view>
                
          <view
            style="width: 1.2rem;height: 1.2rem;"
            un-m="r-4"
          >
            <view
              v-if="selectedValueArr.includes(item.value)"
              un-i="iconfont-check"
            ></view>
          </view>
                
        </view>
          
        <view
          v-if="inited && options4SelectV2Computed.length === 0"
          un-flex="~"
          un-items="center"
          un-justify="center"
          un-text="gray-400"
          un-h="10"
        >
          (暂无数据)
        </view>
        
        <view
          v-else-if="options4SelectV2Computed.length > 5"
          un-m="y-4"
          un-p="x-8"
          un-box-border
        >
          <CustomDivider></CustomDivider>
        </view>
          
      </scroll-view>
      
      <view
        un-p="x-4 y-4"
        un-box-border
        un-flex="~"
        un-w="full"
        un-items="center"
        un-gap="4"
      >
        
        <view
          un-flex="~ [1_0_0]"
        >
          <tm-button
            color="info"
            width="100%"
            @click="onCancel"
          >
            取消
          </tm-button>
        </view>
        
        <view
          un-flex="~ [1_0_0]"
        >
          <tm-button
            width="100%"
            @click="onConfirm"
          >
            确定 ({{ selectedValueArr.length }})
          </tm-button>
        </view>
        
      </view>
      
    </view>
    
  </tm-modal>
</view>
